export interface SpotSearchResult {
  region: Region;
  spot: Region['spots'][number];
}

export interface FoodSearchResult {
  regionId: RegionId;
  index: number;
  region: Region;
  data: RegionFoodData;
  item: FoodItem;
}

export interface EventSearchResult {
  regionId: RegionId;
  region: Region | null;
  event: LocalEvent;
}

export function findRegion(regionId: string | RegionId | null | undefined): Region | null {
  if (!regionId) return null;
  return REGIONS.find((region) => region.id === regionId) || null;
}

export function findSpotByName(name: string): SpotSearchResult | null {
  for (const region of REGIONS) {
    const spot = (region.spots || []).find((item) => item.name === name);
    if (spot) return { region, spot };
  }
  return null;
}

export function findFoodById(foodId: string): FoodSearchResult | null {
  const [regionIdRaw, rawIndex] = String(foodId).split(':');
  const regionId = regionIdRaw as RegionId;
  const index = Number.parseInt(rawIndex, 10);
  const region = findRegion(regionId);
  const data = FOOD_BY_REGION[regionId];
  const item = data?.items?.[index];
  if (!region || !data || !item || Number.isNaN(index)) return null;
  return { regionId, index, region, data, item };
}

export function findSouvenirById(id: string): Souvenir | null {
  return SOUVENIRS.find((item) => item.id === id) || null;
}

export function findEventByTitle(title: string): EventSearchResult | null {
  for (const regionIdRaw of Object.keys(LOCAL_EVENTS)) {
    const regionId = regionIdRaw as RegionId;
    const event = (LOCAL_EVENTS[regionId] || []).find((item) => item.title === title);
    if (event) {
      return {
        regionId,
        region: findRegion(regionId),
        event,
      };
    }
  }
  return null;
}

export function getFavoriteCounts(favorites: Partial<FavoritesState> = {}): FavoriteCounts {
  return {
    spots: (favorites.spots || []).length,
    foods: (favorites.foods || []).length,
    souvenirs: (favorites.souvenirs || []).length,
    events: (favorites.events || []).length,
  };
}

